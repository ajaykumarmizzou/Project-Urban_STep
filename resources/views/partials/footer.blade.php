<script>
    function changeSubHead(value){
        $('#loading').show();
        $('#subhad').show();
        $.ajax({
            type : 'POST',
            url : '{{url('subhead')}}',
            data : {id : value, _token: '{{csrf_token()}}' },
            success : function(res){
                $('#loading').hide();
                $('#subhead').html(res);
            }
        });
    }

    function changeAction(value){
        document.getElementById('change-action').action = value;
    }
</script>
